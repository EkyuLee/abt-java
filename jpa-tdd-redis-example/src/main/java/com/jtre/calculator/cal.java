package com.jtre.calculator;

public class cal {
    public static int added(int x, int y){
        return x + y;
    }
    public static int sub(int x, int y){
        return x - y;
    }
    public static int mul(int x, int y){
        return x * y;
    }
    public static int div(int x, int y){
        return x / y;
    }
    public static int mod(int x, int y){
        return x % y;
    }

}
